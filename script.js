let currentIndex = 0;
const slider = document.getElementById('slider');
const slides = document.querySelectorAll('.slide');
let autoInterval;



function updateSlider() {
  const slideWidth = slides[0].clientWidth + 50;
  slider.scrollLeft = currentIndex * slideWidth;
  console.log(slideWidth)
}


document.querySelector('.prevpg').addEventListener('click', () => {
  if (currentIndex > 0) {
    currentIndex--;
    updateSlider();
  }
});

document.querySelector('.nxtpg').addEventListener('click', () => {
  if (currentIndex < slides.length - 1) {
    currentIndex++;
    updateSlider();
  }
});

function autoslide() {
  startAutoSlide = setInterval(() => {

    if (currentIndex < slides.length - 1) {
      currentIndex++;
    }
    else {
      currentIndex = 0;
    }
    updateSlider();

  }, 3000)
}



